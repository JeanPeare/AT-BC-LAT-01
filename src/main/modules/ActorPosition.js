export class ActorPosition {
    constructor(xPos, yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    getxPos() {
        return this.xPos;
    }

    setxPos(xPos) {
        this.xPos = xPos;
    }

    getyPos() {
        return this.yPos;
    }

    setyPos(yPos) {
        this.yPos = yPos;
    }
}